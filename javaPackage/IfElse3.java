package javaPackage;

public class IfElse3 {

	public static void main(String[] args) {
		
		//to check if else if ladder statement (any one condition will be true) 
		
		int q=300; //Local Variable
		int w=400; //local variable
		
		if(q<w) {
			System.out.println("q is lesser");
		}else if(q==w) {
			System.out.println("q is equal to w");
		}else {
			System.out.println("q is greater");
		}

	}

}
